package org.eclipse.chemclipse.support.settings;

import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * Annotation that could be used to mark a settings class how it behaves in respect to the so called "system.settings"
 *
 */
@Retention(RUNTIME)
@Target(TYPE)
public @interface SystemSettings {

	SystemSettingsStrategy value();

	String dynamicCheckMethod() default "";
}
